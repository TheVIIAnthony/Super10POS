    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String campoNP = campoNombreProducto.getText().toUpperCase();
        String campoMP = campoMarcaProducto.getText().toUpperCase();
        String campoPP = campoPrecioProducto.getText();
        String campoCP = campoCostoProducto.getText();
        String campoPPr = campoPresentacionProducto.getText();
        String campoTPr = tipoPresentacion.getSelectedItem().toString();
        String campoCaP = campoCantidad.getText();
        String sql = "insert into productos (nombreProducto, marcaProducto, precioProducto, costoProducto, presentacion, cantidad) values(?,?,?,?,?,?)";
        if (campoNombreProducto.getText().isEmpty()|| campoMarcaProducto.getText().isEmpty()|| campoPrecioProducto.getText().isEmpty()|| campoCostoProducto.getText().isEmpty()|| campoPresentacionProducto.getText().isEmpty()||tipoPresentacion.equals("selecciona")||campoCantidad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "algunos campos estan vacios");
        } else {
            try {
                int valorCampoPP = Integer.parseInt(campoPP);
                int valorCampoCP = Integer.parseInt(campoCP);
                int valorCampoCa = Integer.parseInt(campoCaP);
                String valorCampoPPr = campoPPr + campoTPr;
                ps = con.prepareStatement(sql);
                ps.setString(1, campoNP);
                ps.setString(2, campoMP);
                ps.setInt(3, valorCampoPP);
                ps.setInt(4, valorCampoCP);
                ps.setString(5, valorCampoPPr);
                ps.setInt(6, valorCampoCa);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Producto dado de alta");
                campoNombreProducto.setText(null);
                campoMarcaProducto.setText(null);
                campoPrecioProducto.setText(null);
                campoCostoProducto.setText(null);
                campoPresentacionProducto.setText(null);
                campoCantidad.setText(null);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

